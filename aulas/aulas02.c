#include <stdio.h> // biblioteca de estrada e saida na console

int main() {
  
  // imprime um texto na tela
  printf("%i\n", 10);
  printf("%i %i\n", 50, 100);
  printf("%5i\n", 1);
  printf("%05i%%\n", 21);
  
  // imprime um numero decimal
  printf("%f\n", 8.5);
  printf("%.3f\n", 3.14159);
  printf("%8.2f\n", 110.5);
  printf("%8.2f\n",2556.86);
  
  // imprime um caractere
  printf("%c\n", 'A');
  printf("%c\n", 68);

  // imprime uma string
  printf("%s\n" , "rei de um mundo sem reis");
  printf("%30s!\n", "quero cafe");
  
    
  
  
  
  
    return 0;
}
